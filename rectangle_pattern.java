import java.util.*;
public class rectangle_pattern {
    public static void main(_String args[]){
      Scanner sc =new Scanner(System.in);
      int i,j;
      for(i=1;i<=4;i++){
        for(j=1;j<=5;j++){
            System.out.print("* ");
        }
        System.out.print("\n");
      }
      sc.close();
    }
    
}
